export function getDemoChatReply(message) {
  const normalized = String(message || "").trim().toLowerCase();
  const matched = ANSWERS.find((item) => item.keywords.some((keyword) => normalized.includes(keyword)));
  if (matched) return matched.text;

  return (
    "Демо-режим: интеллектуальный ассистент не подключён в этой версии проекта. " +
    "Для навигации по порталу используйте меню, поиск по разделам и основные страницы сайта."
  );
}
