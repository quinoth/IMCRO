const absolute = (path) => path?.startsWith("http") ? path : `${SOURCE_BASE_URL}${path}`;
const fileTypeFromHref = (href) => {
  if (!href) return "Документ";
  const clean = href.split("?")[0].toLowerCase();
  if (clean.endsWith(".pdf")) return "PDF";
  if (clean.endsWith(".doc") || clean.endsWith(".docx")) return "DOC";
  if (clean.endsWith(".xls") || clean.endsWith(".xlsx")) return "XLS";
  if (clean.endsWith(".zip")) return "ZIP";
  return href.includes("disk.") || href.includes("yadi.") || href.includes("drive.google") || href.includes("cloud.mail") || href.includes("docs.yandex")
    ? "Файл"
    : "Ссылка";
};
const doc = (title, href, meta) => ({ title, href, meta: meta || fileTypeFromHref(href), kind: "document" });
const pageLink = (title, href) => ({ title, href, meta: "Страница", kind: "page" });
const detail = (label, value, icon, href) => ({ label, value, icon, ...(href ? { href } : {}) });
